<h1 class="title is-1">The revoution of vite!</h1>

<p>
    Vite! French word for "quick", pronounced /vit/, like "veet", is a revolutionary build tool.
</p>
<p>
    It was created by even you blah blah blah..., we all know this stuff, but i would like to point to 3 characteristics that i really like about it!
</p>


<h3 class="title is-3">1-Convetion over configuration</h3>

<p>
    As developers, we dont't want to mess with config files, we need to write code and solve problems.
</p>
<p>
    Vite solves this, respecting conventions, instead of letting you mess around. Mind you, you can do that, but it's not reccomended and desigend so you don't screw up.
</p>
<p>
    I was to afraid to touch svelte beacause of the module bundlers and config files, and now, this blog is powered by svelte and vite (thanks sveltekit).
</p>

<h3 class="title is-3">2-It's all automatic.</h3>

<p>
    If i add dependencies, not only does the load times not slow down, but it automaticlly updates the site, even if it's running.
    I love it, and cloud not live without it... 
</p>

